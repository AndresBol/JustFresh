    private void Txf_NumeroTarjetaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_Txf_NumeroTarjetaKeyReleased
        ETipoTarjeta tipo = validarTipoTarjeta();
        if (tipo == null) {
            Lbl_NumeroTarjeta.setIcon(unknown_Card);
        } else switch (tipo) {
            case VISA -> Lbl_NumeroTarjeta.setIcon(visa_Card);
            case MASTERCARD -> Lbl_NumeroTarjeta.setIcon(master_Card);
        }
    }//GEN-LAST:event_Txf_NumeroTarjetaKeyReleased

