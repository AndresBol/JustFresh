    private boolean validarCampos(){
        
        if(!validarNombre()){
            JOptionPane.showMessageDialog(this, "Digite un nombre valido.", "Advertencia!", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(!validarEmail()){
            JOptionPane.showMessageDialog(this, "Digite un email valido.", "Advertencia!", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(!validarContrasena()){
            JOptionPane.showMessageDialog(this, "Digite una contraseña valida.", "Advertencia!", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if((!validarNumeroTarjeta() || validarTipoTarjeta() == null) && Rdb_UsuarioRegular.isSelected()){
            JOptionPane.showMessageDialog(this, "Digite una tarjeta bancaria valida. (VISA | Mastercard).", "Advertencia!", JOptionPane.ERROR_MESSAGE);
            return false;

        }
        if(!validarCodigoSeguridad() && Rdb_UsuarioRegular.isSelected()){
            JOptionPane.showMessageDialog(this, "Digite un codigo de seguridad valido.", "Advertencia!", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        
        return true;
    }
    
    private boolean validarNombre(){
        return !Txf_Nombre.getText().isEmpty() && Txf_Nombre.getText().length() <= 40;
    }
    
    private boolean validarEmail(){
        return UsuarioDAO.verificarDisponibilidadEmail(Txf_CorreoElectronico.getText()) && !Txf_CorreoElectronico.getText().isEmpty() && Txf_CorreoElectronico.getText().length() <= 320 && Txf_CorreoElectronico.getText().contains("@") && Txf_CorreoElectronico.getText().contains(".");
    }
    
    private boolean validarContrasena(){
        return Psf_Contrasena.getPassword().length > 0 && Psf_Contrasena.getPassword().length <= 50;
    }
    
    private boolean validarNumeroTarjeta(){
        
        String numero = Txf_NumeroTarjeta.getText();
        
        if(numero.isEmpty() || numero.length() != 16){
            return false;
        }
        
        int[] numTarjeta = new int[15];
        int numProvisional = 0, numProvisional2 = 0, suma = 0, validarDigitos = 0;
        
        for(int i = 14; i >= 0; i--){ 
            
            numProvisional = Character.getNumericValue(numero.charAt(i));
            
            if(i % 2 == 0){
                numProvisional2 = numProvisional * 2;
                suma = 0;
                while (numProvisional2 != 0) {
                    suma += numProvisional2 % 10;
                    numProvisional2 = numProvisional2 / 10;
                }
                numTarjeta[i] = suma;
            }else{
                numTarjeta[i] = numProvisional;
            }
        }
        
        
        for(int i = 14; i >= 0; i--){
            validarDigitos += numTarjeta[i];
        } 
        
        validarDigitos *= 9;
        
        return (validarDigitos % 10) == Character.getNumericValue(numero.charAt(15));
    }
    
    private ETipoTarjeta validarTipoTarjeta(){
        if(Txf_NumeroTarjeta.getText().startsWith("4") && validarNumeroTarjeta()){
            return ETipoTarjeta.VISA;
        }else if(Pattern.matches("^(51|52|53|54|55).*", Txf_NumeroTarjeta.getText()) && validarNumeroTarjeta()){
            return ETipoTarjeta.MASTERCARD;
        }else{
            return null;
        }
    }
    
    private boolean validarCodigoSeguridad(){
        int codigo = 0;
        
        try{
            if(Rdb_UsuarioRegular.isSelected()){
                codigo = Integer.parseInt(String.valueOf(Psf_CodigoSeguridad.getPassword()));
            }
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Digite un codigo de seguridad con formato valido.", "Ha sucedido un error!", JOptionPane.ERROR_MESSAGE);
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e, "Ha sucedido un error!", JOptionPane.ERROR_MESSAGE);
        }
        
        return codigo >= 1000 && codigo <= 9999;
    }
    
    private void Btn_RegistrarseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_RegistrarseActionPerformed
        // TODO add your handling code here:
        
        if(validarCampos()){
            
            Usuario usuario;
            
            if(Rdb_Administrador.isSelected()){
                usuario = new UsuarioAdministrador(Txf_CorreoElectronico.getText().toLowerCase(), String.valueOf(Psf_Contrasena.getPassword()), Txf_Nombre.getText(),(Pais) Cmb_PaisOrigen.getSelectedItem(), true);
                
                UsuarioDAO.insertarUsuario(usuario);
            }else if(Rdb_GerenteRestaurante.isSelected()){
                usuario = new UsuarioGerente(Txf_CorreoElectronico.getText().toLowerCase(), String.valueOf(Psf_Contrasena.getPassword()), Txf_Nombre.getText(),(Pais) Cmb_PaisOrigen.getSelectedItem(), true);
                
                UsuarioDAO.insertarUsuario(usuario);
            }else{
                
                Tarjeta tarjeta = new Tarjeta(Long.parseLong(Txf_NumeroTarjeta.getText()), (int) Spn_Mes.getValue(), (int) Spn_Ano.getValue(), Integer.parseInt(String.valueOf(Psf_CodigoSeguridad.getPassword())), validarTipoTarjeta());
                usuario = new UsuarioRegular(Txf_CorreoElectronico.getText().toLowerCase(), String.valueOf(Psf_Contrasena.getPassword()), Txf_Nombre.getText(),(Pais) Cmb_PaisOrigen.getSelectedItem(), true, tarjeta);
                
                UsuarioDAO.insertarUsuario(usuario);
                TarjetaDAO.insertarTarjeta(tarjeta, usuario.getID());
            }
            
            principal.setUsuario(usuario);
            principal.actualizarInfoUsuario();
            this.dispose();
            
        }  
    }//GEN-LAST:event_Btn_RegistrarseActionPerformed

