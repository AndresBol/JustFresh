        if(Psf_Contrasena.getEchoChar() == '\u2022'){
            Psf_Contrasena.setEchoChar((char)0);
            Img_VerContrasena.setIcon(eye_OpenIcon);
        }else{
            Psf_Contrasena.setEchoChar('\u2022');
            Img_VerContrasena.setIcon(eye_ClosedIcon);
        }
        
