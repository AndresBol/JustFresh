    private Producto obtenerProductoIngresado(){
        if(Rdb_Empacados.isSelected()){
            int impuesto = (int) Spn_Impuesto.getValue();
            float impuestoPorcentaje = (float)impuesto / 10;
            
            return new ProductoEmpacado(Txf_Nombre.getText(), Txf_Descripcion.getText(), (float) Spn_Precio.getValue(), (int) Spn_Cantidad.getValue(),impuestoPorcentaje);
        }else{
            return new ProductoPreparado(Txf_Nombre.getText(), Txf_Descripcion.getText(), (float) Spn_Precio.getValue(),  (int) Spn_TiempoPreparacion.getValue());
        }
    }
    
    private void Btn_ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_ActualizarActionPerformed
        // TODO add your handling code here:
        if(validarCampos()){
            
            Producto producto = obtenerProductoIngresado();
            
            ProductoDAO.updateProducto(obtenerProductoSeleccionado().getNombre(), obtenerRestauranteID(), producto);

            principal.actualizarInfoUsuario();

            formatearCampos();
        }
    }//GEN-LAST:event_Btn_ActualizarActionPerformed

    private void Btn_CrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_CrearActionPerformed
        // TODO add your handling code here:
        
        if(validarCampos()){
        
            Producto producto = obtenerProductoIngresado();
                
            ProductoDAO.insertarProducto(producto, obtenerRestauranteID());
            
            principal.actualizarInfoUsuario();

        }  
        
        formatearCampos();
    }//GEN-LAST:event_Btn_CrearActionPerformed

    private void Tbl_ProductosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Tbl_ProductosMouseClicked
        // TODO add your handling code here:
        
        if(evt.getClickCount() >= 1 && Tbl_Productos.isEnabled()){
            
            Tbl_Productos.setEnabled(false);
            
            Producto producto = obtenerProductoSeleccionado();
            
            Cmb_Restaurante.setEnabled(false);
            
            Txf_Nombre.setText(producto.getNombre());
            Txf_Descripcion.setText(producto.getDescripcion());
            Spn_Precio.setValue(producto.getPrecio());
            
            if(producto instanceof ProductoEmpacado){
                ProductoEmpacado productoEmpacado = (ProductoEmpacado) producto;
                
                Rdb_Empacados.setSelected(true);
                Spn_TiempoPreparacion.setValue(1);
                Spn_Cantidad.setValue(productoEmpacado.getCantidad());
                Spn_Impuesto.setValue( (int) (productoEmpacado.getImpuesto() * 10));
            }else{
                ProductoPreparado productoPreparado = (ProductoPreparado) producto;
                
                Rdb_Preparados.setSelected(true);
                Spn_TiempoPreparacion.setValue(productoPreparado.getTiempoPreparacion());
                Spn_Cantidad.setValue(1);
                Spn_Impuesto.setValue(0);
            }
            
            Btn_Crear.setVisible(false);
            Btn_Actualizar.setVisible(true);
            Btn_Cancelar.setVisible(true);
        }
        
    }//GEN-LAST:event_Tbl_ProductosMouseClicked

