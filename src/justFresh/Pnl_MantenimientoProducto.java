    private void formatearCampos(){
        Btn_Crear.setVisible(true);
     
        Btn_Actualizar.setVisible(false);
        Btn_Cancelar.setVisible(false);
        
        Cmb_Restaurante.setEnabled(true);
        Cmb_Restaurante.removeAllItems();
        
        
        if(principal.getUsuario() instanceof UsuarioGerente){
            for(Restaurante restaurante : ((UsuarioGerente) principal.getUsuario()).getRestaurantes()){
                Cmb_Restaurante.addItem(restaurante);
            }
        }else{
            for(Restaurante restaurante : RestauranteDAO.selectRestaurantes()){
                restaurante.setResenas(ResenaDAO.selectResenasRestaurante(restaurante.getID()));
                Cmb_Restaurante.addItem(restaurante);
            }
        }
        
        
        
        Txf_Nombre.setText("");
        Rdb_Preparados.setSelected(true);
        Txf_Descripcion.setText("");
        Spn_Precio.setValue(0);
        Spn_TiempoPreparacion.setValue(1);
        Spn_Cantidad.setValue(1);
        Spn_Impuesto.setValue(0);
        
        Lbl_TiempoPreparacion.setVisible(true);
        Spn_TiempoPreparacion.setVisible(true);
        Lbl_Cantidad.setVisible(false);
        Spn_Cantidad.setVisible(false);
        Lbl_Impuesto.setVisible(false);
        Spn_Impuesto.setVisible(false);
        
        
        Tbl_Productos.setEnabled(true);
        actualizarTabla();
        
    }

