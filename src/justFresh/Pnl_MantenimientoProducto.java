    private void Rdb_PreparadosItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_Rdb_PreparadosItemStateChanged
        // TODO add your handling code here:
        
        if(Rdb_Preparados.isSelected()){
            
            Lbl_TiempoPreparacion.setVisible(true);
            Spn_TiempoPreparacion.setVisible(true);
            Lbl_Cantidad.setVisible(false);
            Spn_Cantidad.setVisible(false);
            Lbl_Impuesto.setVisible(false);
            Spn_Impuesto.setVisible(false);
            
        }else{
            
            Lbl_TiempoPreparacion.setVisible(false);
            Spn_TiempoPreparacion.setVisible(false);
            Lbl_Cantidad.setVisible(true);
            Spn_Cantidad.setVisible(true);
            Lbl_Impuesto.setVisible(true);
            Spn_Impuesto.setVisible(true);
            
        }
    }//GEN-LAST:event_Rdb_PreparadosItemStateChanged


