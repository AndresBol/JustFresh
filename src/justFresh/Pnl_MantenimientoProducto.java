    private boolean validarCampos(){
        
        if(!validarNombre()){
            JOptionPane.showMessageDialog(this, "Digite un nombre valido.", "Advertencia!", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(!validarDescripcion()){
            JOptionPane.showMessageDialog(this, "Digite una localización valida.", "Advertencia!", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        
        return true;
    }
       
    private boolean validarNombre(){
        return !Txf_Nombre.getText().isEmpty() && Txf_Nombre.getText().length() <= 35;
    }
    
    private boolean validarDescripcion(){
        return !Txf_Descripcion.getText().isEmpty() && Txf_Descripcion.getText().length() <= 75;
    }
    
    private int obtenerRestauranteID(){
        Restaurante restaurante = (Restaurante) Cmb_Restaurante.getSelectedItem();
        
        return restaurante.getID();
    }
    
    //Mejora
    private void actualizarTabla(){

        String[] Fila = new String[3];

        modeloTabla.setRowCount(0);

        
        for(Producto producto : ProductoDAO.selectProductos(obtenerRestauranteID())){
            Fila[0] = producto.getNombre();
            Fila[1] = producto instanceof ProductoEmpacado ? "Empacado" : "Preparado";
            Fila[2] = String.valueOf(producto.getPrecio());

            modeloTabla.addRow(Fila);
        }
        
    }
    
    private Producto obtenerProductoSeleccionado(){
        String[] Fila = new String[3];
        Fila[0] = (String) modeloTabla.getValueAt(Tbl_Productos.getSelectedRow(), 0);
        Fila[1] = (String) modeloTabla.getValueAt(Tbl_Productos.getSelectedRow(), 1);
        Fila[2] = (String) modeloTabla.getValueAt(Tbl_Productos.getSelectedRow(), 2);

        String nombre = Fila[0];
        
        return ProductoDAO.selectProducto(nombre, obtenerRestauranteID());
    }
    
