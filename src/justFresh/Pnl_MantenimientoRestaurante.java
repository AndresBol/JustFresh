    private Restaurante obtenerRestauranteIngresado(){
        return new Restaurante(Txf_Nombre.getText(), Txa_Localizacion.getText(), (ETipoRestaurante) Cmb_TipoRestaurante.getSelectedItem());
    }
    
    private void Btn_ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_ActualizarActionPerformed
        // TODO add your handling code here:
        if(validarCampos()){
            
            UsuarioGerente usuario;
        
            if(principal.getUsuario() instanceof UsuarioGerente){
                usuario = (UsuarioGerente) principal.getUsuario();
            }else{
                usuario = (UsuarioGerente) Cmb_UsuarioGerente.getSelectedItem();
            }
            
            Restaurante restaurante = obtenerRestauranteIngresado();
            RestauranteDAO.updateRestaurante(obtenerRestauranteSeleccionado().getNombre(), usuario.getID(), restaurante);

            principal.actualizarInfoUsuario();

            formatearCampos();
        }
    }//GEN-LAST:event_Btn_ActualizarActionPerformed

    private void Btn_CrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_CrearActionPerformed
        // TODO add your handling code here:
        
        if(validarCampos()){
            
            UsuarioGerente usuario;
        
            if(principal.getUsuario() instanceof UsuarioGerente){
                usuario = (UsuarioGerente) principal.getUsuario();
            }else{
                usuario = (UsuarioGerente) Cmb_UsuarioGerente.getSelectedItem();
            }
        
            Restaurante restaurante = obtenerRestauranteIngresado();
                
            RestauranteDAO.insertarRestaurante(restaurante, usuario.getID());
            
            principal.actualizarInfoUsuario();

        }  
        
        formatearCampos();
    }//GEN-LAST:event_Btn_CrearActionPerformed

    private void Tbl_RestaurantesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Tbl_RestaurantesMouseClicked
        // TODO add your handling code here:
        
        if(evt.getClickCount() >= 1 && Tbl_Restaurantes.isEnabled()){
            
            Tbl_Restaurantes.setEnabled(false);
            Cmb_UsuarioGerente.setEnabled(false);
            
            Restaurante restaurante = obtenerRestauranteSeleccionado();
            
            Txf_Nombre.setText(restaurante.getNombre());
            Cmb_TipoRestaurante.setSelectedItem(restaurante.getTipoRestaurante());
            Txa_Localizacion.setText(restaurante.getLocalizacion());
            
            Btn_Crear.setVisible(false);
            Btn_Actualizar.setVisible(true);
            Btn_Cancelar.setVisible(true);
        }
        
    }//GEN-LAST:event_Tbl_RestaurantesMouseClicked

