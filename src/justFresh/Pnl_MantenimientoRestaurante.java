    private boolean validarCampos(){
        
        if(!validarNombre()){
            JOptionPane.showMessageDialog(this, "Digite un nombre valido.", "Advertencia!", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if(!validarDescripcion()){
            JOptionPane.showMessageDialog(this, "Digite una localización valida.", "Advertencia!", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        
        return true;
    }
    //Mejora
    private void actualizarTabla(){
        
        UsuarioGerente usuario;
        
        if(principal.getUsuario() instanceof UsuarioGerente){
            usuario = (UsuarioGerente) principal.getUsuario();
        }else{
            usuario = (UsuarioGerente) Cmb_UsuarioGerente.getSelectedItem();
        }
        
        if(usuario != null){

            String[] Fila = new String[4];

            modeloTabla.setRowCount(0);

            for(Restaurante restaurante : usuario.getRestaurantes()){
                Fila[0] = restaurante.getNombre();
                Fila[1] = restaurante.getTipoRestaurante().toString();
                Fila[2] = String.valueOf(restaurante.getProductos().size());
                Fila[3] = String.valueOf(restaurante.getCalificacion());

                modeloTabla.addRow(Fila);
            }

        }else{
            JOptionPane.showMessageDialog(this, "Porfavor inicie sesión.", "Advertencia!", JOptionPane.ERROR_MESSAGE);
        }
        
    }
    
    private boolean validarNombre(){
        return !Txf_Nombre.getText().isEmpty() && Txf_Nombre.getText().length() <= 35;
    }
    
    private boolean validarDescripcion(){
        return !Txa_Localizacion.getText().isEmpty() && Txa_Localizacion.getText().length() <= 75;
    }
    //Mejora
    private Restaurante obtenerRestauranteSeleccionado(){
        return RestauranteDAO.selectRestaurante((String) modeloTabla.getValueAt(Tbl_Restaurantes.getSelectedRow(), 0));
    }
    
