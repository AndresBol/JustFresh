    private void formatearCampos(){
        Btn_Crear.setVisible(true);
     
        Btn_Actualizar.setVisible(false);
        Btn_Cancelar.setVisible(false);
        
        Cmb_TipoRestaurante.removeAllItems();
        Cmb_UsuarioGerente.setEnabled(true);
        
        if(principal.getUsuario() instanceof UsuarioAdministrador){
            
            Lbl_Gerente.setVisible(true);
            Cmb_UsuarioGerente.setVisible(true);
            Cmb_UsuarioGerente.removeAllItems();
            
        
            for(UsuarioGerente gerente : UsuarioDAO.selectUsuariosGerentes()){
                
                List<Restaurante> restaurantes = new ArrayList();

                for(Restaurante restaurante : RestauranteDAO.selectRestaurantes(gerente.getID())){

                    restaurante.setProductos(ProductoDAO.selectProductos(restaurante.getID()));
                    restaurante.setResenas(ResenaDAO.selectResenasRestaurante(restaurante.getID()));

                    restaurantes.add(restaurante);
                }

                ((UsuarioGerente) gerente).setRestaurantes(restaurantes);

                Cmb_UsuarioGerente.addItem(gerente);

                
            }
        }else{
            Cmb_UsuarioGerente.setVisible(false);
            Lbl_Gerente.setVisible(false);
        }
        
        for(ETipoRestaurante tipo : ETipoRestaurante.values()){
            Cmb_TipoRestaurante.addItem(tipo);
        }
        
        Txf_Nombre.setText("");
        Cmb_TipoRestaurante.setSelectedIndex(0);
        Txa_Localizacion.setText("");
        
        Tbl_Restaurantes.setEnabled(true);
        actualizarTabla();
        
    }

