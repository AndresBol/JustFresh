    private void Tbl_ProductosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Tbl_ProductosMouseClicked
        // TODO add your handling code here:
        if(evt.getClickCount() >= 1){
            Btn_Eliminar.setEnabled(true);
        }
        if(evt.getClickCount() >= 2){
            
            Btn_Eliminar.setEnabled(false);

            String nombreProducto = (String) modeloTabla.getValueAt(Tbl_Productos.getSelectedRow(), 0);
            String cantidadString = JOptionPane.showInputDialog(null, "Modifica la cantidad de " + nombreProducto + " que quieras en el carrito", "Actualizar carrito", JOptionPane.INFORMATION_MESSAGE);
            
            if(cantidadString != null && !cantidadString.isEmpty()){
                try{
                    
                    
                    for(LineaCompra Olinea : lineasDeCompra){
                        if(Olinea.getProducto().getNombre().equals(nombreProducto)){
                            
                            int cantidad = Integer.parseInt(cantidadString);
                            
                            if(cantidad <= 0){
                                JOptionPane.showMessageDialog(this, "Digite un numero mayor a 0", "Advertencia!", JOptionPane.WARNING_MESSAGE);
                                return;
                            }
                            
                            if(Olinea.getProducto() instanceof ProductoEmpacado){
                                
                                if(((ProductoEmpacado) Olinea.getProducto()).getCantidad() == 0){
                                    JOptionPane.showMessageDialog(this, "¡Producto se encuentra agotado!", "Advertencia!", JOptionPane.WARNING_MESSAGE);
                                    return;
                                }
                                
                                if(((ProductoEmpacado) Olinea.getProducto()).getCantidad() < cantidad){
                                    JOptionPane.showMessageDialog(this, "El restaurante no cuenta con " + cantidad + " " + Olinea.getProducto().getNombre(), "Advertencia!", JOptionPane.WARNING_MESSAGE);
                                    return;
                                }
                            }
                            
                            int indice = lineasDeCompra.indexOf(Olinea);
                            LineaCompra linea = new LineaCompra(cantidad, Olinea.getProducto());
                            lineasDeCompra.set(indice, linea);
                            
                            actualizarTabla();
                        }
                    }
                }catch(NumberFormatException e){
                    JOptionPane.showMessageDialog(this, "Digite un formato valido.", "Ha sucedido un error!", JOptionPane.ERROR_MESSAGE);
                }catch(Exception e){
                    JOptionPane.showMessageDialog(this, e, "Ha sucedido un error!", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_Tbl_ProductosMouseClicked

    private void Btn_EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_EliminarActionPerformed
        // TODO add your handling code here:
        
        String nombreProducto = (String) modeloTabla.getValueAt(Tbl_Productos.getSelectedRow(), 0);
        
        for(LineaCompra Olinea : lineasDeCompra){
            if(Olinea.getProducto().getNombre().equals(nombreProducto)){
                int indice = lineasDeCompra.indexOf(Olinea);
                lineasDeCompra.remove(indice);
                actualizarTabla();
                Btn_Eliminar.setEnabled(false);
                return;
            }
        }
        
    }//GEN-LAST:event_Btn_EliminarActionPerformed

    
    

