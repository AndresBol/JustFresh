    private void actualizarTabla(){
        
        String[] Fila = new String[4];
        DecimalFormat df = new DecimalFormat("###,##0.00");

        modeloTabla.setRowCount(0);
        
        if(!lineasDeCompra.isEmpty()){
        
            for(LineaCompra lineaCompra : Pnl_OrdenarPedido.lineasDeCompra){

                Producto producto = lineaCompra.getProducto();

                Fila[0] = producto.getNombre();
                Fila[1] = producto instanceof ProductoEmpacado ? "Empacado" : "Preparado";
                Fila[2] = "₡ " + df.format(producto.getPrecio() * lineaCompra.getCantidad());
                Fila[3] = String.valueOf(lineaCompra.getCantidad());


                modeloTabla.addRow(Fila);
            }
        }
    }

