    private void MnI_CerrarSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MnI_CerrarSesionActionPerformed
        // TODO add your handling code here:
        
        int eleccion = JOptionPane.showConfirmDialog(null, "¿Desea cerrar la sesión?", "Cierre de sesión", JOptionPane.YES_NO_OPTION);
        
        if(eleccion == JOptionPane.YES_OPTION){
            principal.setUsuario(null);
            formatearElementos();
            
            JInternalFrame[] frames = DP_Escritorio.getAllFrames();
            for (JInternalFrame frame : frames) {
                frame.dispose();
            }

            
        }
        
    }//GEN-LAST:event_MnI_CerrarSesionActionPerformed

