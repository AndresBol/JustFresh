    private void MnI_ProductosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MnI_ProductosActionPerformed
        // TODO add your handling code here:
        
        if(principal.getUsuario() instanceof UsuarioGerente){
            if(((UsuarioGerente) principal.getUsuario()).getRestaurantes().isEmpty()){
                JOptionPane.showMessageDialog(this, "¡Usted no cuenta con ningún restaurante con productos!", "JustFresh!", JOptionPane.WARNING_MESSAGE);
            }
        }
        
        UtilitarioVentana.centrarVentanaJFrame(this, "Mantenimiento de Productos", new Pnl_MantenimientoProducto(), DP_Escritorio);
        
    }//GEN-LAST:event_MnI_ProductosActionPerformed

    private void MnI_OrdenesRealizadasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MnI_OrdenesRealizadasActionPerformed
        // TODO add your handling code here:
        
        if(OrdenCompraDAO.OrdenesDeUsuario(principal.getUsuario().getID()).isEmpty()){
            JOptionPane.showMessageDialog(this, "¡Usted no ha realizado ninguna orden de compra!", "JustFresh!", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        UtilitarioVentana.centrarVentanaJFrame(this, "Calificar Ordenes de compra", new Pnl_OrdenesRealizadas(), DP_Escritorio);
        
    }//GEN-LAST:event_MnI_OrdenesRealizadasActionPerformed

