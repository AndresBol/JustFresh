    private void inicioDeSesion(){
        Usuario usuario = principal.getUsuario();
        
        if(usuario != null){
            
            Mn_Cuenta.setText(usuario.getNombre());
            Mn_Cuenta.setIcon(Account_LoggedIcon);
            MnI_CerrarSesion.setVisible(true);
            MnI_Registro.setVisible(false);
            MnI_IniciarSesion.setVisible(false);
            
            if(usuario.getPermisos().contains(EPermiso.MANTENIMIENTO_RESTAURANTE)){
                Mn_Mantenimientos.setVisible(true);
                if(usuario instanceof UsuarioAdministrador){
                    MnI_Paises.setVisible(true);
                    MnI_Usuarios.setVisible(true);
                }
            }
            
            if(usuario.getPermisos().contains(EPermiso.REPORTERIA)){
                Mn_Reporteria.setVisible(true);
            }
            
            if(usuario.getPermisos().contains(EPermiso.ORDENAR_PRODUCTOS)){
                MnI_OrdenarPedido.setVisible(true);
                MnI_OrdenesRealizadas.setVisible(true);
            }
            
        }
    }
    
